#Collection literals in python

# list literal
fruits = ["apple", "mango", "orange"] 
print(fruits)

# tuple literal
numbers = (1, 2, 3) 
print(numbers)

# dictionary literal
alphabets = {'a':'apple', 'b':'ball', 'c':'cat'} #key-value pair
print(alphabets)

# set literal
vowels = {'a', 'e', 'i' , 'o', 'u'} 
